package com.steven.ssh.dto;

public class Teacher {

	private int id;
	private String name;
	private String address;
	private int age;
	private int salary;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	
	@Override
	public String toString() {
		return "Teacher [id=" + id + ", name=" + name + ", address=" + address + ", age=" + age + ", salary=" + salary
				+ "]";
	}
	
}
